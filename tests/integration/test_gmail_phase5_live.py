"""Phase 5 live integration — Gmail write-ops on egor.titt@gmail.com.

Run with:
    LIVE_GOOGLE_TESTS=1 uv run pytest tests/integration/test_gmail_phase5_live.py -v

Safety notes:
  - Drafts are CREATED but never SENT. Test exits with drafts in the user's
    drafts folder; clean up there manually if you want.
  - modify_labels / archive / mark_read tests need a real message to act on.
    Strategy: send a self-message-as-draft via create_draft, then operate on
    that draft's message_id (which Gmail assigns even before send). For full
    integration we need an actual inbox message, so we look up the most
    recent message in the inbox to act on — but ONLY for label-mark tests
    which are reversible.
  - Filters: we create a test filter pointing to a fake address, verify it
    appears in list, delete it. Reversible.
"""
import pytest

pytestmark = pytest.mark.integration


@pytest.fixture(autouse=True)
def _probe_gmail_api(claude_test_account):
    """Skip the module if Gmail API isn't enabled / scopes don't cover the call.

    The user has to manually accept Gmail API's TOS in the Cloud Console
    (`https://console.cloud.google.com/apis/library/gmail.googleapis.com?project=148389149001`)
    before these can run. We probe with a cheap `users().getProfile()` call.
    """
    from src.tools import gmail
    try:
        gmail._service(claude_test_account).users().getProfile(userId="me").execute()
    except Exception as e:
        msg = str(e)
        if "accessNotConfigured" in msg or "has not been used in project" in msg:
            pytest.skip("Gmail API not enabled on the GCP project (accept TOS in Cloud Console)")
        if "insufficient" in msg.lower():
            pytest.skip("Gmail token lacks scope — re-OAuth needed via /accounts UI")
        raise


def _make_inbox_test_message(account: str) -> str:
    """Create + send a draft to self, returning the resulting message_id once
    Gmail has processed it. Used for tests that need a real inbox message
    (modify_labels round-trip). The message ends up in BOTH Sent AND Inbox
    because we email ourselves.
    """
    from src.tools import gmail
    draft = gmail.create_draft(
        to="egor.titt@gmail.com",
        subject="[CLAUDE-TEST phase-5] integration probe",
        body="Auto-generated by Phase 5 integration tests. Safe to delete.",
        account=account,
    )
    sent = gmail.send_draft(draft["draft_id"], account=account)
    return sent["sent_message_id"]


# ---------- get_thread ----------

def test_get_thread_on_self_message(claude_test_account):
    """Create a real inbox message, fetch its thread, ensure structure intact."""
    from src.tools import gmail
    msg_id = _make_inbox_test_message(claude_test_account)
    # Get the message to find its thread_id
    msg = gmail.get_message(msg_id, account=claude_test_account)
    thread_id = msg["thread_id"]
    thread = gmail.get_thread(thread_id, account=claude_test_account)
    assert thread["_meta"]["message_count"] >= 1
    # At least one message should be present and have our subject
    subjects = [m.get("subject", "") for m in thread["messages"]]
    assert any("[CLAUDE-TEST phase-5]" in s for s in subjects)


