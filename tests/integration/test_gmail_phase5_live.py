# ---------- modify_labels round-trip ----------

def test_archive_then_restore_inbox(claude_test_account):
    """Archive a message (remove INBOX), then put it back, verify state."""
    from src.tools import gmail
    msg_id = _make_inbox_test_message(claude_test_account)
    # baseline: should have INBOX
    msg = gmail.get_message(msg_id, account=claude_test_account)
    assert "INBOX" in msg["labels"]

    # archive
    after_archive = gmail.archive(msg_id, account=claude_test_account)
    assert "INBOX" not in after_archive["labels_after"]

    # restore: add INBOX back
    after_restore = gmail.modify_labels(msg_id, add=["INBOX"], account=claude_test_account)
    assert "INBOX" in after_restore["labels_after"]


def test_mark_read_unread_round_trip(claude_test_account):
    from src.tools import gmail
    msg_id = _make_inbox_test_message(claude_test_account)
    # New self-message: likely has UNREAD initially
    after_read = gmail.mark_read(msg_id, account=claude_test_account)
    assert "UNREAD" not in after_read["labels_after"]
    after_unread = gmail.mark_unread(msg_id, account=claude_test_account)
    assert "UNREAD" in after_unread["labels_after"]


# ---------- batch_modify ----------

def test_batch_modify_archives_two_messages(claude_test_account):
    from src.tools import gmail
    m1 = _make_inbox_test_message(claude_test_account)
    m2 = _make_inbox_test_message(claude_test_account)
    result = gmail.batch_modify([m1, m2], remove=["INBOX"], account=claude_test_account)
    assert result["count"] == 2
    # Restore
    gmail.batch_modify([m1, m2], add=["INBOX"], account=claude_test_account)


# ---------- filters ----------

def test_filter_create_list_delete(claude_test_account):
    from src.tools import gmail

    # Create a filter that catches a deliberately-unused subject
    test_subject = "ZZZ-CLAUDE-TEST-FILTER-9d2f"
    created = gmail.create_filter(
        criteria={"subject": test_subject},
        add_labels=["STARRED"],
        account=claude_test_account,
    )
    fid = created["filter_id"]
    try:
        listing = gmail.list_filters(account=claude_test_account)
        ids = [f["id"] for f in listing["filters"]]
        assert fid in ids
    finally:
        gmail.delete_filter(fid, account=claude_test_account)
        # Confirm it's gone
        after = gmail.list_filters(account=claude_test_account)
        assert fid not in [f["id"] for f in after["filters"]]
